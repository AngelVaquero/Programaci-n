let a = 10;
//Relacionales
console.log (a > 5);
console.log (a >= 5);
console.log (a < 5);
console.log (a <= 5);
//Igualdad
console.log(a == 10); // Si es igual exactamente a 10
console.log(a != 10); // Si es distinto de 10
console.log(a == '10'); // El doble igualdad solo pregunta por el valor sin comparar tipos
console.log(a === '10'); // Aquí si compara datos
console.log(a !== '10'); // Esto haría lo mismo pero preguntando sino son iguales los tipos de datos
