let a = 5; // declaración
let b = 7; // declaración

console.log(a + b, 'Adicion'); // expresión // adición
console.log(a - b, 'Resta'); // resta
console.log(a * b, 'Multiplicación'); // multiplicación
console.log(a / b, 'División'); // división
console.log(a % b, 'Módulo'); // módulo Ej: console.log(7 % 5); = 2 por que 5 cabe una vez en 7, y sobran 2 (Sería lo que sobra en una división). Si fuese console.log(5 % 7); = 5 -> Al no caber generaría una "," y añadiríamos un 0 al 5, por lo tanto lo que sobraría sería el 5
console.log(b % a, 'Módulo');
console.log( a ** b, 'Potencia'); // potencia Sería "a" elevado a "b"

//Incrementar
console.log(++a); 
console.log(a++);
console.log(a);
//Decrementar
console.log(--a); 
console.log(a--);
